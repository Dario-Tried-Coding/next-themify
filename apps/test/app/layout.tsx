import { ThemeProvider } from '@dariotriedcoding/next-themify'
import { Keys } from '@dariotriedcoding/next-themify/types'
import type { Metadata } from 'next'

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
}

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode
}>) {
  const keys = {mode: {custom: ['custom1', 'custom2']}} as const satisfies Keys

  return (
    <html lang='en' suppressHydrationWarning>
      <body>
        <ThemeProvider<typeof keys>
          config={{
            mode: {
              strategy: 'light_dark',
              enableSystem: true,
              default: 'system',
              fallback: 'light',
              keys: {
                dark: 'dark',
                light: 'light',
                system: 'system',
                custom: [
                  { key: 'custom1', colorScheme: 'dark' },
                  { key: 'custom2', colorScheme: 'light' },
                ],
              },
            },
            theme: {
              strategy: 'mono',
              key: 'default',
            },
          }}
        >
          {children}
        </ThemeProvider>
      </body>
    </html>
  )
}
