import { ThemeProvider } from '@dariotriedcoding/next-themify'
import { Keys } from '@dariotriedcoding/next-themify/types'
import type { Metadata } from 'next'

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
}

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode
}>) {
  const keys = {mode: {dark: 'prova-dark'}, theme: 'prova-theme'} as const satisfies Keys

  return (
    <html lang='en' suppressHydrationWarning>
      <body>
        <ThemeProvider<typeof keys>
          config={{
            mode: {
              strategy: 'light_dark',
              enableSystem: false,
              default: 'prova-dark',
              keys: {
                light: 'light',
                dark: 'prova-dark',
              }
            },
            theme: {
              strategy: 'mono',
              key: 'prova-theme',
            },
            radius: {
              strategy: 'mono',
              key: 'default'
            }
          }}
        >
          {children}
        </ThemeProvider>
      </body>
    </html>
  )
}
